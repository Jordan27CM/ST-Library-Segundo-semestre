    private void btnRegistrarseRegistro2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRegistrarseRegistro2MouseClicked
        Usuario usuario = new Usuario();
        String nickName = usuarioRegistro2.getText().toLowerCase();
        String clave1 = String.valueOf(passClave1Registro2.getPassword());
        String clave2 = String.valueOf(passClave2Registro2.getPassword());
        if(nickName.isEmpty() || nickName.contains(" ")){
            msmRegistroUsuario2.setForeground(Color.red);
            msmRegistroUsuario2.setText("El nombre de usuario no puede estar vacio ni tener espacios");
        }
        
        else if(!validaciones.validarClaves(clave1, clave2)){
            msmRegistroUsuario2.setForeground(Color.red);
            msmRegistroUsuario2.setText("Las contraseas no coinciden o no cumplen con el minimo de 8 caracteres");
        }
        
        else if(!validaciones.validarNickname(nickName)){
            msmRegistroUsuario2.setForeground(Color.red);
            msmRegistroUsuario2.setText("el nombre de usuario ya existe intente con otro");
        }
        
        else{
            try {
                usuario.setNickname(nickName);
                usuario.setClave(clave1);
                usuario.setNombre(nombreUsuarioRegistro.getText());
                usuario.setApellido(apellidoUsuarioRegistro.getText());
                usuario.setRut(rutUsuarioRegistro.getText());
                dao.getDataUsuario().agregar(usuario);
                panelBtnVolverLoginRegistro2.setVisible(true);
                msmRegistroUsuario2.setForeground(Color.blue);
                msmRegistroUsuario2.setText("Usuario registrado con exito vuelva al login");
            } catch (SQLException ex) {
                msmRegistroUsuario2.setForeground(Color.red);
                msmRegistroUsuario2.setText("Algo salio mal intentelo nuevamente");
            }
        }
    }//GEN-LAST:event_btnRegistrarseRegistro2MouseClicked

