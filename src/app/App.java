    private void btnSiguienteRegistroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSiguienteRegistroMouseClicked
        String nombre = nombreUsuarioRegistro.getText();
        String apellido = apellidoUsuarioRegistro.getText();
        String rut = rutUsuarioRegistro.getText();
        if(apellido.isEmpty() || nombre.isEmpty()){
            msmRegistroUsuario1.setForeground(Color.red);
            msmRegistroUsuario1.setText("Los campos no pueden estar vacios");
        }
        else if(!validaciones.validarRut(rut) ){//rut.equals("25569152-k")
            msmRegistroUsuario1.setForeground(Color.red);
            msmRegistroUsuario1.setText("Rut Invalido");
        }
        
        else if(!validaciones.validarRutRepetitivo(rutUsuarioRegistro.getText())){
            msmRegistroUsuario1.setForeground(Color.red);
            msmRegistroUsuario1.setText("Ya existe un usuario registrado con ese rut");
        }
        
        else{
            registro.setVisible(false);
            registro2.setVisible(true);
            login.setVisible(false);
            loginEmpleado.setVisible(false);
        }
    }//GEN-LAST:event_btnSiguienteRegistroMouseClicked

